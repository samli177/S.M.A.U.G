    public boolean sendSteerCommand(int angle, int rotation, int speed) {
        // 0 <= angle < 180
        // 0 <= speed <= 100
        // 0 <= rotation <= 100
        byte data[] = new byte[3];
        data[0] = (byte) angle;
        data[1] = (byte) rotation;
        data[2] = (byte) speed;

        return sendData('C', data);
    }

