    private int crc16(int length, byte[] data) {
        int crc = 0xFFFF;
        int poly = 0x8408;

        if (length == 0) {
            return 0;
        }
        for (int i = length - 1; i >= 0; i--) {
            byte b = data[i];
            for (int j = 0; j < 8; j++) {
                if ((crc & 0x0001) != ((b >> j) & 0x0001)) {
                    crc = (crc >> 1) ^ poly;
                } else {
                    crc >>= 1;
                }
            }
        }

        crc ^= 0xFFFF;
        int clone = crc;
        crc = (crc << 8) | ((clone >> 8) & 0x00FF);
        return crc;
    }

