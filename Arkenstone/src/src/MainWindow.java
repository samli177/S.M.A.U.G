    private void robotSteeringUpdate() {
        float x = 0, y = 0, xrot = 0;

        if (controller != null) {
            for (Component c : controller.getComponents()) {
                switch (c.getName()) {
                    case "X-axeln":
                        x = c.getPollData();
                        break;
                    case "Y-axeln":
                        y = c.getPollData();
                        break;
                    case "X-rotation":
                        xrot = c.getPollData();
                        break;
                }
            }
        }

        int speed;
        float angle = 0;
        if (arrowsAngle >= 0) {
            angle = arrowsAngle;
            speed = 100;
        } else if (dPadValue != 0) {
            angle = 360 * (1 - dPadValue) + 90;
            if (angle >= 360) {
                angle -= 360;
            }
            speed = 100;
        } else {
            angle = (float) Math.toDegrees(Math.atan2(x, y)) - 180;
            if (angle < 0) {
                angle += 360;
            } else if (angle >= 360) {
                angle -= 360;
            }
            speed = (int) Math.round(Math.sqrt(x * x + y * y) * 100);
            if (speed < MIN_SPEED) {
                speed = 0;
            } else if (speed > 100) {
                speed = 100;
            }
        }

        int rotation;
        if (rotationValue != 0) {
            rotation = rotationValue;
        } else {
            rotation = Math.round(50 * xrot);
            if (Math.abs(rotation) < MIN_ROTATION) {
                rotation = 0;
            }
        }

        if (speed > 0 || Math.abs(rotation) > 0) {
            // För att kunna skicka över UART
            angle /= 4;
            rotation += 50;
            System.out.println("Direction: " + (int) angle + ", rotation: " + rotation + ", speed: " + speed);
            sendSteerCommand((int) angle, rotation, speed);
        }
    }
