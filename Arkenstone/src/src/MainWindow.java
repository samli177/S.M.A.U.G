    private void lowerDrawAreaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lowerDrawAreaKeyPressed
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_UP:
                if (!keyUpPressed) {
                    keyUpPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_DOWN:
                if (!keyDownPressed) {
                    keyDownPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_LEFT:
                if (!keyLeftPressed) {
                    keyLeftPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_RIGHT:
                if (!keyRightPressed) {
                    keyRightPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_NUMPAD0:
                if (!keyZeroPressed) {
                    keyZeroPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_CONTROL:
                if (!keyControlPressed) {
                    keyControlPressed = true;
                    keyUpdate();
                }
                break;
            case KeyEvent.VK_ADD:
                if (!keyRaisePressed) {
                    keyRaisePressed = true;
                    sendElevationCommand(true);
                }
                break;
            case KeyEvent.VK_SUBTRACT:
                if (!keyLowerPressed) {
                    keyLowerPressed = true;
                    sendElevationCommand(false);
                }
                break;
        }
    }//GEN-LAST:event_lowerDrawAreaKeyPressed

